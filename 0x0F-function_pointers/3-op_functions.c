#include "3-clac.h"
/**
 * op_add - adds int
 * @a: int
 * @b: int
 * Return: 0
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - sub int
 * @a: int
 * @b: int
 * Return: 0
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - mul int
 * @a: int
 * @b: int
 * Return: 0
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - div int
 * @a: int
 * @b: int
 * Return: 0
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - mod int
 * @a: int
 * @b: int
 * Return: 0
 */
int op_mod(int a, int b)
{
	return (a % b);
}
